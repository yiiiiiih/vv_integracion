package com.practica.integracion.manager;

public class SystemManagerException extends Exception {

	private static final long serialVersionUID = 2464349448453554404L;

	public SystemManagerException() {
		super();
	}

	public SystemManagerException(String message, Throwable cause) {
		super(message, cause);
	}

	public SystemManagerException(String message) {
		super(message);
	}

	public SystemManagerException(Throwable cause) {
		super(cause);
	}

	
	
}
